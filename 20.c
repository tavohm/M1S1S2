#include <stdio.h>

int main()
{
    char tablero[8][8] =
        {
            {'T','C','A',' ',' ','A','C','T'},
            {'P','P','P','P','P','P','P','P'},
            {' ',' ',' ',' ',' ',' ',' ',' '},
            {' ',' ',' ',' ',' ',' ',' ',' '},
            {' ',' ',' ',' ',' ',' ',' ',' '},
            {' ',' ',' ',' ',' ',' ',' ',' '},
            {'P','P','P','P','P','P','P','P'},
            {'T','C','A',' ',' ','A','C','T'}
        };

    int f,c;

    for(f=0; f<8; f++)
    {
        for(c=0; c<8; c++)
        {
            printf("%c ",tablero[f][c]);
        }
        printf("\n");
    }

    return 0;
}